List p = 18f4520
    #include<p18f4520.inc>
    CONFIG OSC = INTIO67
    CONFIG WDT = OFF
    org 0x00 ; PC = 0x00
    
    initial:
	MOVLW 0x25 ; a1 = 1
	MOVWF 0x10
	MOVLW 0x1F ; a2 = 0
	MOVWF 0x11
	MOVLW 0x1D ; a3 = 0
	MOVWF 0x12
	MOVLW 0x30 ; a4 = 1
	MOVWF 0x13
	MOVLW 0x04 ; b1 = 3
	MOVWF 0x20
	MOVLW 0x03 ; b2 = 3
	MOVWF 0x21
	MOVLW 0x02 ; b3 = 1
	MOVWF 0x22
	MOVLW 0x01 ; b4 = 4
	MOVWF 0x23
    start:
	MOVFF 0x10, 0x40
	MOVFF 0x11, 0x41
	MOVFF 0x20, 0x42
	MOVFF 0x22, 0x43
	rcall multiply
	MOVFF 0x32, 0x00
	
	MOVFF 0x10, 0x40
	MOVFF 0x11, 0x41
	MOVFF 0x21, 0x42
	MOVFF 0x23, 0x43
	rcall multiply
	MOVFF 0x32, 0x01
	
	MOVFF 0x12, 0x40
	MOVFF 0x13, 0x41
	MOVFF 0x20, 0x42
	MOVFF 0x22, 0x43
	rcall multiply
	MOVFF 0x32, 0x02
	
	MOVFF 0x12, 0x40
	MOVFF 0x13, 0x41
	MOVFF 0x21, 0x42
	MOVFF 0x23, 0x43
	rcall multiply
	MOVFF 0x32, 0x03
	
	goto finish
    multiply:
	MOVF 0x40, WREG ; ax
	MULWF 0x42 ; ax * bx
	MOVFF PRODL, 0x32
	MOVF 0x41, WREG ; ay
	MULWF 0x43 ; ay * by
	MOVF PRODL, WREG
	ADDWF 0x32, f ; [0x32] = answer
	RETURN
    finish:
	
    end